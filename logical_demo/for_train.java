package logical_demo;

public class for_train {
    /*
    for(变量初始化；循环条件表达式；循环后的操作表达式）
    {
    执行语句
    }
     */

    //注：
    public static void main(String[] args) {
        int x=1;
        for (System.out.println("a");x<3;System.out.println("c")) {
            System.out.println("d");
            x++;
        }

    }
}
