package logical_demo;

public class while_train {
    public static void main(String[] args) {
        /*


        while(条件语句)
        {
        执行语句；
        }


        先执行，后判断。。循环体至少执行依次。
        do
        {
        执行语句

        }while(田间语句)；

         */
        int x = 1;
        while (x < 10) {
            x+=1;
            System.out.println(x);
        }

    }
}
