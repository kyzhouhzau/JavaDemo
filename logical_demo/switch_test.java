package logical_demo;

public class switch_test {

    public static void main(String[] args) {
        int week = 1;
        switch (week) {
            case 1:
                System.out.println("星期一");
                break;
            case 2:
                System.out.println("星期二");
                break;
            default:
                System.out.println("others");
                break;
        }

    }
}



